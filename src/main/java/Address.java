
public class Address {
private int id;
private int empId;
private String address;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getEmpId() {
	return empId;
}
public void setEmpId(int empId) {
	this.empId = empId;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}


}
